/*
    Uma classe é um molde ou uma receita para criar objetos. Ela define as características
    (atributos) e comportamentos (métodos) que os objetos criados a partir dela terão. A
    classe é como um plano que descreve como os objetos devem ser estruturados e como eles
    devem se comportar.

    Por exemplo:
    - Exemplo 1: se tivermos uma classe "Celular", ela pode ter atributos como "marca",
     "modelo", "cor" e "capacidade da bateria", e ações como "ligar", "desligar",
     "tirarFoto" e "enviarMensagem". A classe "Celular" é apenas uma definição
     abstrata, e não representa um celular específico, mas sim a ideia geral de um celular.

    - Exemplo 2: se tivermos uma classe "Personagem", ela pode ter atributos como
     "nome", "idade" e "força", e ações como "atacar" e "defender". A classe
     "Personagem" é uma definição geral de um personagem, e não representa um
     personagem específico, mas sim a ideia geral de um personagem.

    Em resumo, uma classe é uma definição abstrata que serve como base para criar
    objetos concretos, enquanto um objeto é uma instância específica dessa classe
    com suas próprias características e comportamentos.
 */

