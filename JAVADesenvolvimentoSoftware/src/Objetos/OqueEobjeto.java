package Objetos;

/*
    Um objeto é um "trem" que tem características (atributos) e comportamentos (métodos).
    Ele é uma instância de uma classe, que é como um molde ou uma receita para criar objetos.
    Por exemplo, se tivermos uma classe "Carro", ela pode ter atributos como "cor", "modelo"
    e "ano", e métodos como "acelerar" e "frear". Quando criamos um objeto a partir dessa
    classe, como "meuCarro", ele terá suas próprias características (por exemplo, cor vermelha,
    modelo sedan, ano 2020) e poderá executar os comportamentos definidos na classe (acelerar,
    frear). Em resumo, um objeto é uma entidade concreta que representa algo do mundo real ou
    conceitual, com suas próprias propriedades e ações.

    Exemplo: Celular
    Caracteristicas: marca, modelo, cor, capacidade da bateria
    Atributos: marca, modelo, cor, capacidade da bateria
    Métodos: ligar, desligar, tirarFoto, enviarMensagem

 */